"use client";

import { motion, AnimatePresence } from "framer-motion";
import { useState, useEffect } from "react";
import { HeroBanner } from "@/lib/services/heroBanner.service";
import { useRouter } from "next/navigation";
import { vibrate } from "@/lib/haptics";
import Image from "next/image";

interface HeroCarouselProps {
    banners: HeroBanner[];
    autoScrollInterval?: number;
}

export function HeroCarousel({ banners, autoScrollInterval = 5000 }: HeroCarouselProps) {
    const router = useRouter();
    const [currentIndex, setCurrentIndex] = useState(0);
    const [direction, setDirection] = useState<'left' | 'right'>('left');
    const [isDragging, setIsDragging] = useState(false);

    // Default fallback banners if none provided
    const displayBanners = banners.length > 0 ? banners : [
        {
            id: 'fallback-1',
            title: 'Welcome to BackBenchers',
            subtitle: 'Exclusive deals for students',
            ctaText: 'Explore',
            ctaLink: '/dashboard/explore',
            backgroundGradient: 'from-[#1a1a1a] via-[#222] to-black',
            bannerType: 'announcement',
            isActive: true,
            position: 0,
            imageUrl: null,
            logoUrl: undefined,
            coverageType: 'pan_india',
            cityIds: null,
            startDate: new Date().toISOString(),
            endDate: null,
            isAutoGenerated: false,
            merchantId: undefined,
            eventDate: null,
            eventLocation: null,
            organizerName: null,
            organizerContact: null
        } as HeroBanner
    ];

    useEffect(() => {
        if (displayBanners.length <= 1 || isDragging) return;

        const interval = setInterval(() => {
            setDirection('left');
            setCurrentIndex((prev) => (prev + 1) % displayBanners.length);
        }, autoScrollInterval);

        return () => clearInterval(interval);
    }, [displayBanners.length, autoScrollInterval, isDragging]);

    const paginate = (newDirection: number) => {
        setDirection(newDirection > 0 ? 'left' : 'right');
        setCurrentIndex((prev) => (prev + newDirection + displayBanners.length) % displayBanners.length);
    };

    const swipeConfidenceThreshold = 10000;
    const swipePower = (offset: number, velocity: number) => {
        return Math.abs(offset) * velocity;
    };

    const handleDragEnd = (e: any, { offset, velocity }: any) => {
        setIsDragging(false);
        const swipe = swipePower(offset.x, velocity.x);

        if (swipe < -swipeConfidenceThreshold) {
            paginate(1);
        } else if (swipe > swipeConfidenceThreshold) {
            paginate(-1);
        }
    };

    const handleBannerClick = (banner: HeroBanner) => {
        if (isDragging) return; // Prevent click on drag
        vibrate('light');
        if (banner.ctaLink) {
            router.push(banner.ctaLink);
        }
    };

    const currentBanner = displayBanners[currentIndex];

    // Animation Variants
    const variants = {
        enter: (direction: 'left' | 'right') => ({
            x: direction === 'left' ? 1000 : -1000,
            opacity: 0
        }),
        center: {
            zIndex: 1,
            x: 0,
            opacity: 1
        },
        exit: (direction: 'left' | 'right') => ({
            zIndex: 0,
            x: direction === 'left' ? -1000 : 1000,
            opacity: 0
        })
    };

    return (
        <div className="relative overflow-hidden w-full touch-pan-y py-4">
            <div className="mx-5 relative z-10">
                <AnimatePresence mode="popLayout" initial={false} custom={direction}>
                    <motion.div
                        key={currentBanner.id}
                        custom={direction}
                        variants={variants}
                        initial="enter"
                        animate="center"
                        exit="exit"
                        transition={{
                            x: { type: "spring", stiffness: 300, damping: 30 },
                            opacity: { duration: 0.2 }
                        }}
                        drag="x"
                        dragConstraints={{ left: 0, right: 0 }}
                        dragElastic={1}
                        onDragStart={() => setIsDragging(true)}
                        onDragEnd={handleDragEnd}
                        onClick={() => handleBannerClick(currentBanner)}
                        className={`
                            relative w-full aspect-[2/1] min-h-[190px] max-h-[230px] rounded-2xl 
                            bg-gradient-to-br ${currentBanner.backgroundGradient || 'from-[#111] to-black'} 
                            flex flex-col items-center justify-center text-center p-6 
                            shadow-2xl overflow-hidden cursor-pointer select-none
                            ring-1 ring-white/10
                        `}
                    >
                        {/* Background Image (Cover) */}
                        {currentBanner.imageUrl && (
                            <div className="absolute inset-0 z-0">
                                <Image
                                    src={currentBanner.imageUrl}
                                    className="object-cover"
                                    fill
                                    priority
                                    alt="Hero Banner"
                                    draggable={false}
                                    sizes="(max-width: 768px) 100vw, 800px"
                                />
                                <div className="absolute inset-0 bg-black/60 backdrop-blur-[1px]" /> {/* Premium blur overlay */}
                            </div>
                        )}

                        {/* Content Container - Logo on Left, Text on Right */}
                        <div className="relative z-10 flex items-center gap-6 w-full h-full px-6">

                            {/* Left Side: Logo */}
                            {currentBanner.logoUrl && (
                                <div className="flex-shrink-0 relative">
                                    <div className="h-20 w-20 rounded-2xl bg-white shadow-2xl p-2 border border-white/30">
                                        <Image
                                            src={currentBanner.logoUrl}
                                            alt="Partner Logo"
                                            fill
                                            className="object-contain p-1"
                                            draggable={false}
                                            sizes="80px"
                                        />
                                    </div>
                                </div>
                            )}

                            {/* Right Side: Text & CTA */}
                            <div className="flex-1 flex flex-col items-start gap-2">
                                {/* Partnership Badge */}
                                {currentBanner.organizerName && (
                                    <div className="px-3 py-1 bg-white/10 border border-white/20 rounded-full backdrop-blur-md">
                                        <span className="text-[10px] font-black text-white tracking-widest uppercase flex items-center gap-1.5">
                                            BACKBENCHERS <span className="text-white/50">×</span> {currentBanner.organizerName}
                                        </span>
                                    </div>
                                )}

                                {/* New Store Badge */}
                                {!currentBanner.organizerName && currentBanner.bannerType === 'new_store' && (
                                    <div className="px-3 py-1 bg-green-500/20 border border-green-500/30 rounded-full">
                                        <span className="text-[10px] font-bold text-green-400 tracking-wider uppercase">NEW PARTNER</span>
                                    </div>
                                )}

                                {/* Title */}
                                <h2 className="text-white font-extrabold text-xl md:text-2xl tracking-tight leading-tight drop-shadow-md text-left">
                                    {currentBanner.title}
                                </h2>

                                {/* Subtitle */}
                                {currentBanner.subtitle && (
                                    <p className="text-white/70 text-xs font-medium leading-relaxed text-left max-w-[240px]">
                                        {currentBanner.subtitle}
                                    </p>
                                )}

                                {/* CTA Button */}
                                <button className="mt-1 px-5 py-2 bg-white hover:bg-white/90 text-black text-xs font-bold rounded-full transition-all active:scale-95 flex items-center gap-1.5 group shadow-xl">
                                    {currentBanner.ctaText}
                                    <span className="group-hover:translate-x-0.5 transition-transform">→</span>
                                </button>
                            </div>
                        </div>
                    </motion.div>
                </AnimatePresence>
            </div>

            {/* Pagination Dots */}
            {displayBanners.length > 1 && (
                <div className="flex justify-center gap-2 mt-5">
                    {displayBanners.map((_, idx) => (
                        <button
                            key={idx}
                            onClick={() => {
                                setDirection(idx > currentIndex ? 'left' : 'right');
                                setCurrentIndex(idx);
                            }}
                            className={`
                                h-1.5 rounded-full transition-all duration-300 
                                ${idx === currentIndex ? 'w-6 bg-white shadow-[0_0_10px_rgba(255,255,255,0.5)]' : 'w-1.5 bg-white/20'}
                            `}
                        />
                    ))}
                </div>
            )}
        </div>
    );
}
